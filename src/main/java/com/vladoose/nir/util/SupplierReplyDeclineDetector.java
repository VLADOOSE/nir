package com.vladoose.nir.util;

import java.util.regex.Pattern;

/**
 * Эвристика «поставщик отказался»: распознаёт в ответе на КП фразы вида «данную позицию мы не
 * поставляем», «снята с производства», «не занимаемся». Чистая функция без I/O. Консервативна —
 * как {@link SupplierReplyPriceParser}, «не гадает»: слабые сигналы («к сожалению», «на рынке
 * достаточно поставщиков») НЕ триггерят, только явные формулы отказа. Сначала отрезает
 * процитированный оригинал ({@link EmailReplyText}), чтобы не сработать на нашем же тексте.
 */
public final class SupplierReplyDeclineDetector {

    // (?iU): UNICODE_CHARACTER_CLASS — иначе \w не матчит кириллицу (сн[яи]\w+ не поймал бы «снята»)
    private static final Pattern DECLINE = Pattern.compile("(?iU)("
            + "не\\s+поставля\\w*"                              // не поставляем/поставляет/поставляют
            + "|не\\s+поставим"
            + "|не\\s+(можем|сможем|будем|готовы)\\s+поставит\\w*"
            + "|поставка\\s+(данной\\s+позиции\\s+)?невозможна"
            + "|сн[яи]\\w+\\s+с\\s+производства"               // снят(а)/сняты/снимается с производства
            + "|не\\s+занимаемся"
            + "|не\\s+работаем\\s+с\\b"
            + "|не\\s+располагаем\\s+(данн|такой|этой)"
            + "|нет\\s+в\\s+ассортименте"
            + "|не\\s+производ[иья]\\w*"                        // не производим/производится
            + "|данн\\w+\\s+позици\\w+\\s+не\\s+"               // «данную позицию не …»
            + ")");

    private SupplierReplyDeclineDetector() {}

    /** true, если тело ответа (без нашей цитаты) содержит явную формулу отказа поставить. */
    public static boolean isDecline(String rawBody) {
        String text = EmailReplyText.stripToReply(rawBody);
        if (text.isBlank()) return false;
        return DECLINE.matcher(text).find();
    }
}
